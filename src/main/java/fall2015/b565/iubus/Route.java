package fall2015.b565.iubus;

import java.sql.Time;
import java.util.Date;

public class Route {
    private int busId;
    private int routeId;
    private Time[] timesOfDay;
    private Date date;
    private Time startTime;

    public Route(Date date, Time startTime) {
        this.date = date;
        this.startTime = startTime;
    }

    public int getBusId() {
        return busId;
    }

    public void setBusId(int busId) {
        this.busId = busId;
    }

    public int getRouteId() {
        return routeId;
    }

    public void setRouteId(int routeId) {
        this.routeId = routeId;
    }

    public Time[] getTimesOfDay() {
        return timesOfDay;
    }

    public void setTimesOfDay(Time[] timesOfDay) {
        this.timesOfDay = timesOfDay;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Time getStartTime() {
        return startTime;
    }

    public void setStartTime(Time startTime) {
        this.startTime = startTime;
    }
}
